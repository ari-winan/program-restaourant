    int[] listHarga;
    public void setListHarga(ArrayList<MenuPesan> al){
        listHarga = new int[al.size()];
        int index=0;
        for(MenuPesan mp:al){
            listHarga[index]=mp.getHarga();
            index++;
        }
    }
    public void cari(int opsi){
        ArrayList<MenuPesan> al = new ArrayList<>();
        if(opsi==0) al=con.cariMenu(id.getText(), opsi);
        else al=con.cariMenu(nama.getText(), opsi);
        if(al.isEmpty()){
            JOptionPane.showMessageDialog(this,"Tidak ditemukan","Peringatan!",JOptionPane.WARNING_MESSAGE);
            if(opsi==0){
                if(nama.getText().equals("NAMA")) nama.requestFocus();
            }
            else{
                if(id.getText().equals("ID")) id.requestFocus();
            }
        }
        else{
            if(opsi==0){
                id.setText(al.get(0).getId());
                nama.setText(al.get(0).getNama());
                unLockIdNama(false);
                satuan.requestFocus();
                harga.setText(String.valueOf(al.get(0).getHarga()));
            }
            setListHarga(al);
            menuTable(al);
        }
    }
    
    ArrayList<MenuPesan> addOn;
    public void setAddOn(){
        PriceAdd.setVisible(false);
        this.addOn = con.addOn();
        addon.removeAllItems();
        addon.addItem("ADD-ON");
        for(MenuPesan mp:addOn)
            addon.addItem(mp.getNama());
    }
    
    public void unLockIdNama(boolean status){
        jenis.setEnabled(status);
        id.setEditable(status);
        nama.setEditable(status);
        click = !status;
        priceAddOn.setVisible(!status);
        setAddOn();
    }
    public void menuTable(ArrayList<MenuPesan> al){
        DefaultTableModel model = (DefaultTableModel) menu.getModel();
        Object baris[] = new Object[3];
        model.setRowCount(0);
        for(MenuPesan mp:al){
            baris[0] = mp.getId();
            baris[1] = mp.getNama();
            baris[2] = mp.getHarga();
            model.addRow(baris);
        }
    }
    
    int[] indeks;
    ArrayList<TabelPesanan> untukPesanan;
    public void tabelPesanan(ArrayList<TabelPesanan> al){
        untukPesanan = al;
        indeks = new int[al.size()];
        DefaultTableModel model = (DefaultTableModel) burgera.getModel();
        Object baris[] = new Object[5];
        model.setRowCount(0);
        int total=0;
        int i = 0;
        for(TabelPesanan tp:al){
            indeks[i] = tp.getIndeks();
            baris[0] = tp.getNama();
            baris[1] = tp.getAddon();
            baris[2] = tp.getSatuan();
            baris[3] = tp.getHarga();
            baris[4] = tp.getHarga()*tp.getSatuan();
            total += (int) baris[4]; //object bisa cast(ubah dengan kurung), tanpa parse atau valueOf
            model.addRow(baris);
            i++;
        }
        this.total.setText("Rp."+String.valueOf(total));
    }
    
