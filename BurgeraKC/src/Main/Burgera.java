    private void inputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inputActionPerformed
        if(click&&!pesananClick){
            if(satuan.getText().equals("SATUAN")){
                JOptionPane.showMessageDialog(this,"Masukkan Satuan Terlebih dahulu","Peringatan!",JOptionPane.WARNING_MESSAGE);
                satuan.requestFocus();
            }
            else if(makan.getSelectedItem().equals("MAKAN DI?")){
                JOptionPane.showMessageDialog(this,"Tentukan makan di mana","Peringatan!",JOptionPane.WARNING_MESSAGE);
                makan.requestFocus();
            }
            else{
                int satuan = Integer.valueOf(this.satuan.getText()), harga = Integer.valueOf(this.harga.getText()), addHarga=0;
                String id = this.id.getText(), addNama="", idKasir = setgetKasir.user,tanggal=setgetKasir.getDateForDB();
                if(addon.getSelectedIndex()==0){
                    addNama="";
                    addHarga=0;
                }
                else{
                    addHarga=this.addOn.get(this.addon.getSelectedIndex()-1).getHarga();
                    addNama=addon.getSelectedItem().toString();
                }
                banyakPesanan++;
                hargaAddOndpsn.add(banyakPesanan,addHarga);
                con.inputPesanan(pesanan.getText(), idKasir, id, addNama, satuan, harga+addHarga, makan.getSelectedItem().toString(), tanggal);
                tabelPesanan(con.showPesanan(pesanan.getText()));
                clear();
            }
        }
        else if(pesananClick){
            JOptionPane.showMessageDialog(this, "Tidak boleh tekan Input","Peringatan!",JOptionPane.WARNING_MESSAGE);
            perbarui.requestFocus();
        }
        else{
            JOptionPane.showMessageDialog(this,"Masukkan ID atau Nama terlebih dahulu","Peringatan!",JOptionPane.WARNING_MESSAGE);
            if(id.getText().equals("ID"))id.requestFocus();
            else if(nama.getText().equals("NAMA")) nama.requestFocus();
            else cari.requestFocus();
        }
    }//GEN-LAST:event_inputActionPerformed
    int banyakPesanan=-1;
